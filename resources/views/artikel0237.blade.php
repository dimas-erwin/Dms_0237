    <h1 style="text-align:center">Framework</h1>
    <h3>Apa Itu Framework?</h3>
    <p>Framework adalah sebuah software untuk memudahkan para programer untuk membuat sebuah aplikasi web yang di dalam nya 
    ada berbagai fungsi diantaranya plugin, dan konsep untuk membentuk suatu sistem tertentu agar tersusun dan terstruktur dengan rapi.</p>
    <h3>Apa Saja Jenis Framework?</h3>
    <p>Untuk saat ini framework terbagi dua yaitu framework PHP dan framework CSS.</p>
    <h3>Apa Saja Yang Termasuk Framework PHP?</h3>
    <p>Laravel, Yii, Code Igniter, Symfoni, Zend Framework, Cake PHP, Fuel PHP</p>
    <h3>Apa Saja Yang Termasuk Framework CSS?</h3>
    <p>Bootstrap, Gumby, Foundation, Less Framework, JQuery UI, Unsemantic, Blue Print CSS</p>
